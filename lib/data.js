function calcHotScore(base, minsAgo, trust) {
  const authority = { A: 1.0, B: 0.85, C: 0.6 };
  return Math.round(base * Math.pow(0.99, minsAgo / 30) * (authority[trust] || 0.85));
}

function loadData(domain, cat, raw) {
  return raw.map(function(row, i) {
    var s = SOURCES[row[1]] || {color:'#888',label:'??',full:row[1]};
    return {
      rank: i + 1,
      title: row[0],
      source: row[1],
      source_full: s.full,
      source_color: s.color,
      source_label: s.label,
      trust: row[2],
      heat: row[3],
      mins_ago: row[4],
      url: row[5],
      cluster: row[6] || null,
      debunk: row[7] || false,
      hot_score: calcHotScore(row[3], row[4], row[2])
    };
  });
}

